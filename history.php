learn. As time passed, the trust supplemented the school with
additional classrooms, teaching equipment, facilities for sports and
activities, qualified teachers and supporting staff. This resulted in a
versatile education with both curricular and extracurricular
expertise. The students were imbibed with values, ethics and skills
