Sadwa and nearby areas. They utilised the support of Srijan
Shiksha Sansthan, a trust founded with the purpose of increasing
the literacy rate. Under the guidance of Late Dr. Kaushal Kishore
Tripathi, a renowned academic of Prayagraj, they set out to give easy
access to education in areas where there is a shortage of schools and
educators. Local leaders and experienced persons were consulted to
understand the needs of the community.
For the purpose of developing this institution, they constructed a
building on NH-27, 24 KM milestone, Rewa Road, Prayagraj, Uttar
Pradesh 212107, on a piece of land already donated to the trust.
This land is situated 5 KMs from the nearest railway station (Jasra)
and 3 KMs from the nearest bus station (Gohaniya).
Within a short span of time, the school received wide public
support because the institution is recognised by Basic Shiksha
Parishad, Uttar Pradesh. On April 1st 2019, the journey began with
a small group of teachers and fifty young minds, who were eager to
learn. As time passed, the trust supplemented the school with
additional classrooms, teaching equipment, facilities for sports and
activities, qualified teachers and supporting staff. This resulted in a
versatile education with both curricular and extracurricular
expertise. The students were imbibed with values, ethics, and skills
to support their overall development into well-rounded individuals.
The trust and the school work in sync to continuously improve and
innovate in order to boost ahead of the traditional methodology in
the field of education. </p>
